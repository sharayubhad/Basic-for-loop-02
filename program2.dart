void main(){
  int x=2;
  switch(x){
    case 1:
    print("violet");
    case 2:
    print("Indigo");
    case 3:
    print("Blue");
    case 4:
    print("Green");
    case 5:
    print("Yellow");
    case 6:
    print("Orange");
    case 7:
    print("Red");
    default:
    print("Do not show any color");
  }
}